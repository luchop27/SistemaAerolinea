    public void setDatos(Avion ob) {
        jTextField1.setText(String.valueOf(ob.getIdAvion()));
        jTextField2.setText(ob.getMarca());
        jTextField3.setText(ob.getModelo());
        jTextField4.setText(ob.getMatricula());
        jTextField5.setText(String.valueOf(ob.getCapacidad()));
        jComboBox1.setSelectedItem(ob.getPiloto());
        jComboBox2.setSelectedItem(ob.getCopiloto());
        jComboBox3.setSelectedItem(ob.getAzafata());
        
    }
    private void jLabel29MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel29MouseClicked
        // TODO add your handling code here:
        jPanel33.setBackground(new Color(67,67,67));
        AvionDAO opln = new AvionDAO();
        
        int id = Integer.parseInt(jTextField1.getText());
        String marca = jTextField2.getText();
        String model = jTextField3.getText();
        String matri = jTextField4.getText();
        int cap = Integer.parseInt(jTextField5.getText());
        String piloto = jComboBox1.getSelectedItem().toString();
        String copiloto = jComboBox2.getSelectedItem().toString();
        String azafata = jComboBox3.getSelectedItem().toString();
        if (opln.verificarTripulantesAsignados(id, piloto, copiloto, azafata)) {
    JOptionPane.showMessageDialog(null, "Error: Los tripulantes ya est치n asignados a otro avi칩n.");
} else {
        Avion ob = new Avion(id, marca, model, matri, cap, piloto, copiloto, azafata);

        if (!jTextField1.getText().equals("") && !jTextField2.getText().equals("") && !jTextField3.getText().equals("")
                && !jTextField4.getText().equals("") && !jTextField5.getText().equals("")
                && jComboBox1.getSelectedIndex() >= 0 && jComboBox2.getSelectedIndex() >= 0 && jComboBox3.getSelectedIndex() >= 0) {

            if (funcion.equals("Insertar")) {
                // Insertar avi칩n
                opln.InsertarAvion(ob);
            } else {
                // Modificar avi칩n
                opln.ModificarAvion(ob);
            }
            Inicio.MostrarTablaAvion();

            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese todos los datos"); 
        }}
    }//GEN-LAST:event_jLabel29MouseClicked


