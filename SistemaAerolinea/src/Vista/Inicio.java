     private Object[] obtenerInfoAvionDesdeTabla(int filaSeleccionada) {
        if (filaSeleccionada >= 0 && filaSeleccionada < jTable5.getRowCount()) {
            try {
                int idAvion = Integer.parseInt(jTable5.getValueAt(filaSeleccionada, 0).toString());
                int capacidad = Integer.parseInt(jTable5.getValueAt(filaSeleccionada, 4).toString());

                return new Object[]{idAvion, capacidad};
            } catch (NumberFormatException e) {
                System.err.println("Error al convertir valores de la tabla a enteros: " + e.getMessage());
            }
        }
        return null;
    }
    private void realizarVuelo() {
    int filaSeleccionadaVuelo = jTable6.getSelectedRow();

    if (filaSeleccionadaVuelo != -1) {
        try {
            int idAvionVuelo = Integer.parseInt(jTable6.getValueAt(filaSeleccionadaVuelo, 0).toString());
            int filaSeleccionadaAvion = buscarFilaPorId(jTable5, idAvionVuelo);
            Object[] infoAvion = obtenerInfoAvionDesdeTabla(filaSeleccionadaAvion);

            if (infoAvion != null) {
                int idAvionTabla = (int) infoAvion[0];

                if (idAvionVuelo == idAvionTabla) {
                    int capacidadAvion = (int) infoAvion[1];

                    PasajeroDAO pasajeroDAO = new PasajeroDAO();
                    int sumaAsientos = pasajeroDAO.obtenerSumaAsientosPorVuelo(idAvionVuelo);

                    int capacidadTotal = capacidadAvion + sumaAsientos;

                    jTable5.setValueAt(capacidadTotal, filaSeleccionadaAvion, 4);

                    AvionDAO avionDAO = new AvionDAO();
                    boolean actualizacionEnBD = avionDAO.actualizarCapacidadAvion(idAvionVuelo, capacidadTotal);

                    if (actualizacionEnBD) {
                        System.out.println("Capacidad del avin actualizada en la base de datos.");
                    } else {
                        System.out.println("Error al actualizar la capacidad del avin en la base de datos.");
                    }

                    System.out.println("Capacidad total para el vuelo con ID " + idAvionVuelo + ": " + capacidadTotal);
                } else {
                    System.out.println("El idAvion del vuelo no coincide con el de jTable5.");
                }
            } else {
                System.out.println("No se encontr informacin del avin en jTable5.");
            }
        } catch (NumberFormatException e) {
            System.err.println("Error al convertir el ID del avin del vuelo a entero: " + e.getMessage());
        }
    } else {
        System.out.println("Seleccione un vuelo para realizar la operacin.");
    }
}

    private int buscarFilaPorId(JTable table, int id) {
        for (int i = 0; i < table.getRowCount(); i++) {
            int idTabla = Integer.parseInt(table.getValueAt(i, 0).toString());
            if (id == idTabla) {
                return i;
            }
        }
        return -1;  // No se encontr el ID en la tabla
    }
     
